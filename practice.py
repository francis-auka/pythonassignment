my_list = []

# 1. Append elements
my_list.append(10)
my_list.append(20)
my_list.append(30)
my_list.append(40)

# 2. Insert 15 at the second position
my_list.insert(1, 15)

# 3. Extend the list
my_list.extend([50, 60, 70])

# 4. Remove the last element
my_list.pop()

# 5. Sort the list
my_list.sort()

# 6. Find and print the index of 30
print("Index of 30:", my_list.index(30))
